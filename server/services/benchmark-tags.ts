export function normalizeUnit(rawUnit: string | null | undefined): string | null {
  if (!rawUnit) return null;
  const cleaned = String(rawUnit).trim().toLowerCase().replace(/\s+/g, " ");
  if (!cleaned) return null;
  if (UNIT_NORMALIZATION_MAP[cleaned]) return UNIT_NORMALIZATION_MAP[cleaned];
  const noPunct = cleaned.replace(/[.\s]/g, "");
  if (UNIT_NORMALIZATION_MAP[noPunct]) return UNIT_NORMALIZATION_MAP[noPunct];
  return null;
}
